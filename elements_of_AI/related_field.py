# The Turing test
# Alan Turing (1912-1954) was an English mathematician and logician. He is rightfully considered to be the father of
# computer science. Turing was fascinated by intelligence and thinking, and the possibility of simulating them by machines.
# Turing’s most prominent contribution to AI is his imitation game, which later became known as the Turing test.
# In the test, a human interrogator interacts with two players, A and B, by exchanging written messages (in a chat).
# If the interrogator cannot determine which player, A or B, is a computer and which is a human, the computer is said to pass
# the test. The argument is that if a computer is indistinguishable from a human in a general natural language conversation, then
# it must have reached human-level intelligence.