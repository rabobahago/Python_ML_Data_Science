# Three types of machine learning
# The roots of machine learning are in statistics which can also be thought of as the art of extracting
# knowledge from data. Especially methods such as linear regression and Bayesian statistics, which are
# both already more than two centuries old (!), are even today at the heart of machine learning.

# Supervised learning: We are given an input, for example a photograph with a traffic sign, and the task
# is to predict the correct output or label, for example which traffic sign is in the picture
# (speed limit, stop sign, etc.). In the simplest cases, the answers are in the form of yes/no
# (we call these binary classification problems).

# Unsupervised learning: There are no labels or correct outputs. The task is to discover the
# structure of the data: for example, grouping similar items to form “clusters”, or reducing
# the data to a small number of important “dimensions”. Data visualization can also be considered unsupervised learning.

# Reinforcement learning: Commonly used in situations where an AI agent like a self-driving car must operate in an
# environment and where feedback about good or bad choices is available with some delay. Also used in games where the
# outcome may be decided only at the end of the game.

# The categories are somewhat overlapping and fuzzy, so a particular method can sometimes be hard to place in one
# category. For example, as the name suggests, so-called semisupervised learning is partly supervised and partly unsupervised.

